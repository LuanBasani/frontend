// Funções Matémáticas (math)


// SQRT e POW (raiz quadrada e potência)

//calcular a raiz quadrada de 25
console.log(Math.sqrt(25)); //5

//Potência de nº pelo Outro
console.log(Math.pow(7,2)); //7² = 49
console.log(Math.pow(4,3)); //4³ = 64
console.log(Math.pow(27,1/3)); // ³√27 = 3

// Funçoes de arredondamento(round, ceil, floor)
console.log(Math.round(4.7)); //5
console.log(Math.round(4.3)); //4
console.log(Math.ceil(4.3)); //5
console.log(Math.floor(4.7)); //4

// Funções Números Aleatórios(random)
console.log(Math.random()); //gera um número aleatório entre 0 e 1
// Gerar um número aleatório entre 0 e 1000
console.log(Math.random() * 1000); //gera um número aleatório entre 0 e 1000 

//Números Absolutos (abs)
console.log(Math.abs(-10)); //10

// Funções Analíticas Minimo e Máximo (min e max)
console.log(Math.min(5, 10, 2, 8)); //2
console.log(Math.max(5, 10, 2, 8)); //10

